/// Class containing the supported color schemes.
class ColorSchemes {
  static const primaryColorScheme = ColorScheme.light(
    // Primary colors
    primary: Color(0XFF9F3247),
    primaryContainer: Color(0XFFF9F9F9),
    secondaryContainer: Color(0X0F9F3247),

    // On colors(text colors)
    onPrimary: Color(0X0FFFFFFF),
    onPrimaryContainer: Color(0XFF7A2334),
  );
}

